 # Подсчитать сумму цифр в вещественном числе.

print('Задача 4 => Подсчитать сумму цифр в вещественном числе.')
print('ПЕРВЫЙ ВАРИАНТ') 

Num = abs(float(input("Введите вещественное число: ")))

Arr = list(str(Num).split('.'))
summ = 0
for i in Arr:
    for j in i:
        summ += int(j)
print(f"Сумма цифр вещественного числа равна = {summ}")
print()

