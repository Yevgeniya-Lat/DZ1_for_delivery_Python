N = int(input("Введите N для набора произведений чисел от 1 до N: "))

def set_product_num(n):
    num = 1
    set = []
    for i in range(1, n+1):
        num *= i
        set.append(num)
    return set

print(f"Набор произведений чисел от 1 до {N}:  {set_product_num(N)}")    



